//======================================================
// PdfProcessor.cpp
//
// Координатор роботи програми.
//
// Цей модуль відповідає за загальну
// послідовність обробки:
//
// 1. Конвертація Excel у CSV.
// 2. Зчитування CSV.
// 3. Пошук PDF.
// 4. Передача кожного PDF у PdfWorker.
// 5. Повідомлення GUI про прогрес.
// 6. Запис журналу.
//
// ВАЖЛИВО.
//
// PdfProcessor нічого не знає
// про Qt.
//
// Він не використовує:
//
// • QObject
// • QThread
// • signals
// • slots
//
// Єдиний спосіб повідомити GUI
// про новий результат — callback.
//
//======================================================

#include "PdfProcessor.h"

#include <filesystem>
#include <iostream>

#include "CsvReader.h"
#include "ExcelConverter.h"
#include "FileScanner.h"
#include "PdfWorker.h"
#include "ResultWriter.h"

namespace fs = std::filesystem;

//======================================================
// Обробити папку.
//
// folder
//      Папка з PDF.
//
// excelFile
//      Excel-файл.
//
// callback
//      Викликається після обробки
//      кожного PDF.
//
//======================================================

std::vector<PdfInfo> processFolder(
    const std::string& folder,
    const std::string& excelFile,
    ProgressCallback callback)
{
    //--------------------------------------------------
    // Побудувати шлях до CSV.
    //
    // Наприклад:
    //
    // тест.xlsx
    //
    // ->
    //
    // тест.csv
    //--------------------------------------------------

    fs::path excel(excelFile);

    std::string csvFile =
        (excel.parent_path() /
        (excel.stem().string() + ".csv")).string();

    //--------------------------------------------------
    // Конвертувати Excel.
    //--------------------------------------------------

    std::cout
        << "\n=== Конвертація Excel ===\n";

    if (!convertExcelToCsv(excelFile, csvFile))
    {
        std::cout
            << "Помилка конвертації Excel\n";

        return {};
    }

    //--------------------------------------------------
    // Зчитати CSV.
    //--------------------------------------------------

    auto excelTable =
        readCsv(csvFile);

    //--------------------------------------------------
    // Просканувати папку.
    //--------------------------------------------------

    auto pdfFiles =
        scanPdfFiles(folder);

    //--------------------------------------------------
    // Кількість PDF.
    //
    // Потрібна для ProgressBar.
    //--------------------------------------------------

    const int total =
        static_cast<int>(pdfFiles.size());

    int current = 0;

    //--------------------------------------------------
    // Обробити всі PDF.
    //--------------------------------------------------

    for (auto& pdf : pdfFiles)
    {
        //----------------------------------------------
        // Обробити один PDF.
        //----------------------------------------------

        processPdf(
            pdf,
            folder,
            excelTable);

        //----------------------------------------------
        // Ще один PDF завершено.
        //----------------------------------------------

        ++current;

        //----------------------------------------------
        // Якщо callback заданий,
        // повідомити про результат.
        //
        // PdfProcessor нічого
        // не знає про GUI.
        //
        // Він просто викликає
        // функцію callback().
        //----------------------------------------------

        if (callback)
        {
            callback(
                current,
                total,
                pdf);
        }
    }

    //--------------------------------------------------
    // Записати журнал.
    //--------------------------------------------------

    writeResult(
        "logs/result.csv",
        pdfFiles);

    //--------------------------------------------------
    // Повернути результати.
    //
    // Це потрібно для випадків,
    // коли GUI захоче отримати
    // повний список після
    // завершення роботи.
    //--------------------------------------------------

    return pdfFiles;
}