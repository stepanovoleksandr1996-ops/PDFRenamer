//======================================================
// ProcessingWorker.cpp
//
// Робітник Qt.
//
// Цей клас працює у окремому потоці.
//
// Він НЕ виконує обробку PDF самостійно.
//
// Його задача:
//
// • викликати processFolder();
//
// • отримувати callback після
//   кожного PDF;
//
// • перетворювати callback
//   у сигнали Qt;
//
// • повідомити MainWindow,
//   що робота завершена.
//
//======================================================

#include "ProcessingWorker.h"

#include "PdfProcessor.h"

#include <QString>

//======================================================
// Конструктор.
//
//======================================================

ProcessingWorker::ProcessingWorker(QObject *parent)
    : QObject(parent)
{
}

//======================================================
// Передати шляхи.
//
// folder
//      папка з PDF.
//
// excel
//      Excel-файл.
//
//======================================================

void ProcessingWorker::setPaths(
    const std::string& folder,
    const std::string& excel)
{
    folderPath = folder;
    excelFile = excel;
}

//======================================================
// process()
//
// Виконується у окремому потоці.
//
//======================================================

void ProcessingWorker::process()
{
    //--------------------------------------------------
    // Запустити ядро.
    //
    // PdfProcessor після
    // кожного PDF викликатиме
    // callback().
    //--------------------------------------------------

    processFolder(
        folderPath,
        excelFile,

        //--------------------------------------------------
        // Callback.
        //
        // current
        // total
        // pdf
        //--------------------------------------------------

        [this](int current,
               int total,
               const PdfInfo& pdf)
        {
            //------------------------------------------
            // Повідомлення журналу.
            //------------------------------------------

            QString message =
                QString::fromStdString(
                    pdf.fileName +
                    " -> " +
                    pdf.status);

            emit logMessage(message);

            //------------------------------------------
            // ProgressBar.
            //------------------------------------------

            if(total > 0)
            {
                emit progressChanged(
                    current * 100 / total);
            }
        });

    //--------------------------------------------------
    // Повідомити,
    // що вся робота завершена.
    //--------------------------------------------------

    emit finished();
}