//======================================================
// MainWindow.cpp
//
// Головне вікно програми PDFRenamer.
//
// Цей файл відповідає ТІЛЬКИ за графічний інтерфейс.
//
// Тут НЕ виконується:
//
// • читання PDF;
// • пошук ІПН;
// • читання Excel;
// • перейменування файлів.
//
// Вся ця логіка знаходиться в:
//
// PdfProcessor
//      ↓
// PdfWorker
//
// MainWindow лише:
//
// • будує вікно;
// • реагує на натискання кнопок;
// • запускає робочий потік;
// • отримує повідомлення від ProcessingWorker;
// • показує їх користувачу.
//
// Такий підхід називається
// "розділення відповідальності".
//
// Завдяки цьому:
//
// GUI можна повністю змінити,
// не переписуючи ядро програми.
//
//======================================================

#include "MainWindow.h"

//------------------------------------------------------
// Робітник.
//
// Саме він запускає ядро програми
// у окремому потоці.
//
//------------------------------------------------------

#include "ProcessingWorker.h"

//------------------------------------------------------
// Заголовки Qt.
//
// QApplication
//      головний застосунок Qt.
//
// QFileDialog
//      діалог вибору папки та файлу.
//
// QLabel
//      текстові підписи.
//
// QLineEdit
//      поля введення.
//
// QMessageBox
//      повідомлення про помилки.
//
// QPlainTextEdit
//      журнал роботи.
//
// QProgressBar
//      смуга прогресу.
//
// QPushButton
//      кнопки.
//
// QThread
//      окремий потік.
//
//------------------------------------------------------

#include <QApplication>
#include <QFileDialog>
#include <QGridLayout>
#include <QLabel>
#include <QLineEdit>
#include <QMessageBox>
#include <QPlainTextEdit>
#include <QProgressBar>
#include <QPushButton>
#include <QThread>
#include <QVBoxLayout>
#include <QWidget>

//======================================================
// Конструктор MainWindow.
//
// Викликається лише один раз
// під час запуску програми.
//
// Саме тут:
//
// • створюється інтерфейс;
//
// • підключаються всі кнопки.
//
// Тут ще НЕ запускається
// обробка PDF.
//
//======================================================

MainWindow::MainWindow(QWidget *parent)
    : QMainWindow(parent),
      workerThread(nullptr),
      worker(nullptr),
      processingCompleted(false)
{
    //--------------------------------------------------
    // Побудувати графічний інтерфейс.
    //
    // Після виклику buildUi()
    // всі елементи вже існують.
    //--------------------------------------------------

    buildUi();

    //--------------------------------------------------
    // Натиснули кнопку
    // "Огляд..." біля папки.
    //
    // Викличеться selectFolder().
    //--------------------------------------------------

    connect(folderButton,
            &QPushButton::clicked,
            this,
            &MainWindow::selectFolder);

    //--------------------------------------------------
    // Натиснули кнопку
    // "Огляд..." біля Excel.
    //--------------------------------------------------

    connect(excelButton,
            &QPushButton::clicked,
            this,
            &MainWindow::selectExcel);

    //--------------------------------------------------
    // Натиснули кнопку
    // "Почати".
    //
    // Саме звідси починається
    // вся обробка PDF.
    //--------------------------------------------------

    connect(startButton,
            &QPushButton::clicked,
            this,
            &MainWindow::startProcessing);
}

//======================================================
// Деструктор.
//
// Викликається при закритті програми.
//
// Якщо робочий потік ще існує,
// його потрібно коректно завершити.
//
// Чому це важливо?
//
// Якщо цього не зробити,
// програма може завершитися,
// поки другий потік ще працює.
//
// Це одна з найпоширеніших
// причин аварійного завершення
// Qt-програм.
//
//======================================================

MainWindow::~MainWindow()
{
    //--------------------------------------------------
    // Якщо потік був створений —
    // попросити його завершитися.
    //--------------------------------------------------

    if(workerThread)
    {
        //--------------------------------------------------
        // Надіслати сигнал завершення.
        //--------------------------------------------------

        workerThread->quit();

        //--------------------------------------------------
        // Дочекатися,
        // поки потік повністю
        // завершить роботу.
        //--------------------------------------------------

        workerThread->wait();
    }
}
//======================================================
// Побудова графічного інтерфейсу.
//
// Ця функція викликається лише один раз
// із конструктора MainWindow.
//
// Саме тут створюються всі елементи:
//
// • поля введення;
// • кнопки;
// • журнал;
// • ProgressBar;
// • статус.
//
// Після завершення buildUi()
// вікно повністю готове до роботи.
//
//======================================================

void MainWindow::buildUi()
{
    //--------------------------------------------------
    // Центральний віджет.
    //
    // У Qt кожне головне вікно повинно
    // містити центральний віджет.
    //
    // Саме всередині нього буде
    // розташований весь інтерфейс.
    //--------------------------------------------------

    QWidget *central = new QWidget(this);

    setCentralWidget(central);

    //--------------------------------------------------
    // Заголовок вікна.
    //--------------------------------------------------

    setWindowTitle("PDF Renamer");

    //--------------------------------------------------
    // Початковий розмір.
    //
    // Користувач зможе його
    // змінювати вручну.
    //--------------------------------------------------

    resize(800, 600);

    //--------------------------------------------------
    // Головний вертикальний Layout.
    //
    // Він розташовує елементи
    // зверху вниз.
    //
    // Схематично:
    //
    // ------------------------
    // Папка
    // Excel
    // ------------------------
    // Журнал
    // ------------------------
    // ProgressBar
    // ------------------------
    // Статус
    // ------------------------
    // Кнопка
    // ------------------------
    //--------------------------------------------------

    auto *layout = new QVBoxLayout;

    //--------------------------------------------------
    // GridLayout.
    //
    // Використовується для двох рядків:
    //
    // Папка:
    // [___________] [Огляд]
    //
    // Excel:
    // [___________] [Огляд]
    //
    //--------------------------------------------------

    auto *grid = new QGridLayout;

    //--------------------------------------------------
    // ---------- ПАПКА ----------
    //--------------------------------------------------

    grid->addWidget(
        new QLabel("Папка з PDF:"),
        0,
        0);

    folderEdit = new QLineEdit;

    grid->addWidget(
        folderEdit,
        0,
        1);

    folderButton =
        new QPushButton("Огляд...");

    grid->addWidget(
        folderButton,
        0,
        2);

    //--------------------------------------------------
    // ---------- EXCEL ----------
    //--------------------------------------------------

    grid->addWidget(
        new QLabel("Excel:"),
        1,
        0);

    excelEdit = new QLineEdit;

    grid->addWidget(
        excelEdit,
        1,
        1);

    excelButton =
        new QPushButton("Огляд...");

    grid->addWidget(
        excelButton,
        1,
        2);

    //--------------------------------------------------
    // Додати GridLayout
    // у головний Layout.
    //--------------------------------------------------

    layout->addLayout(grid);

    //--------------------------------------------------
    // ---------- ЖУРНАЛ ----------
    //
    // Тут буде показуватись
    // вся інформація про обробку.
    //
    // Наприклад:
    //
    // 1.pdf -> RENAMED
    // 2.pdf -> DUPLICATE
    // ...
    //
    //--------------------------------------------------

    layout->addWidget(
        new QLabel("Журнал"));

    logEdit =
        new QPlainTextEdit;

    //--------------------------------------------------
    // Користувач не повинен
    // редагувати журнал.
    //--------------------------------------------------

    logEdit->setReadOnly(true);

    layout->addWidget(logEdit);

    //--------------------------------------------------
    // ---------- PROGRESS BAR ----------
    //
    // Початкове значення = 0%.
    //
    // Надалі ProcessingWorker
    // буде надсилати сигнал:
    //
    // progressChanged(...)
    //
    //--------------------------------------------------

    progressBar =
        new QProgressBar;

    progressBar->setRange(0, 100);

    progressBar->setValue(0);

    layout->addWidget(progressBar);

    //--------------------------------------------------
    // ---------- СТАТУС ----------
    //
    // Показує:
    //
    // Готовий
    // Обробка...
    // Завершено
    //
    //--------------------------------------------------

    statusLabel =
        new QLabel("Готовий");

    layout->addWidget(statusLabel);

    //--------------------------------------------------
    // ---------- КНОПКА ----------
    //
    // Після натискання
    // запускається
    // startProcessing().
    //
    //--------------------------------------------------

    startButton =
        new QPushButton("Почати");

    layout->addWidget(startButton);

    //--------------------------------------------------
    // Встановити Layout
    // центральному віджету.
    //
    // Після цього Qt сам
    // розмістить усі елементи.
    //--------------------------------------------------

    central->setLayout(layout);
}
//======================================================
// Вибір папки.
//
// Викликається після натискання:
//
// "Огляд..."
//
// біля поля "Папка з PDF".
//
//======================================================

void MainWindow::selectFolder()
{
    //--------------------------------------------------
    // Відкрити стандартний діалог
    // вибору папки.
    //
    // QFileDialog — готовий компонент Qt.
    //
    // Він автоматично використовує
    // стиль Windows або Linux.
    //--------------------------------------------------

    QString folder =
        QFileDialog::getExistingDirectory(
            this,
            "Виберіть папку з PDF");

    //--------------------------------------------------
    // Якщо користувач натиснув
    // "Скасувати",
    // рядок буде порожнім.
    //--------------------------------------------------

    if(!folder.isEmpty())
    {
        //--------------------------------------------------
        // Показати шлях
        // у полі введення.
        //--------------------------------------------------

        folderEdit->setText(folder);
    }
}

//======================================================
// Вибір Excel.
//
// Викликається після натискання:
//
// "Огляд..."
//
// біля поля Excel.
//
//======================================================

void MainWindow::selectExcel()
{
    //--------------------------------------------------
    // Відкрити стандартний
    // діалог вибору файлу.
    //--------------------------------------------------

    QString file =
        QFileDialog::getOpenFileName(
            this,
            "Виберіть Excel",
            "",
            "Excel (*.xlsx *.xls)");

    //--------------------------------------------------
    // Якщо файл вибраний —
    // показати його шлях.
    //--------------------------------------------------

    if(!file.isEmpty())
    {
        excelEdit->setText(file);
    }
}

//======================================================
// Натиснули кнопку
// "Почати".
//
// Саме звідси запускається
// вся програма.
//
// Послідовність:
//
// 1. Перевірити,
//    чи вибрана папка.
//
// 2. Перевірити,
//    чи вибраний Excel.
//
// 3. Очистити журнал.
//
// 4. Створити QThread.
//
// 5. Створити ProcessingWorker.
//
// 6. Передати йому шляхи.
//
// 7. Перемістити його
//    у окремий потік.
//
// 8. Підключити сигнали.
//
// 9. Запустити потік.
//
//======================================================

void MainWindow::startProcessing()
{
        //--------------------------------------------------
    // Якщо обробка вже завершена,
    // кнопка працює як
    // "Завершити".
    //--------------------------------------------------

    if (processingCompleted)
    {
        close();
        return;
    }
    //--------------------------------------------------
    // Перевірити папку.
    //--------------------------------------------------

    if(folderEdit->text().isEmpty())
    {
        QMessageBox::warning(
            this,
            "Помилка",
            "Оберіть папку з PDF.");

        return;
    }

    //--------------------------------------------------
    // Перевірити Excel.
    //--------------------------------------------------

    if(excelEdit->text().isEmpty())
    {
        QMessageBox::warning(
            this,
            "Помилка",
            "Оберіть Excel.");

        return;
    }

    //--------------------------------------------------
    // Очистити журнал.
    //
    // Якщо цього не зробити,
    // новий запуск буде
    // дописувати інформацію
    // до попереднього журналу.
    //--------------------------------------------------

    logEdit->clear();

    //--------------------------------------------------
    // Почати прогрес
    // з нуля.
    //--------------------------------------------------

    progressBar->setValue(0);

    //--------------------------------------------------
    // Показати,
    // що програма працює.
    //--------------------------------------------------

    statusLabel->setText("Обробка...");

    //--------------------------------------------------
    // Заборонити повторне
    // натискання кнопки.
    //
    // Інакше користувач
    // може випадково
    // створити другий потік.
    //--------------------------------------------------

    startButton->setEnabled(false);

    //--------------------------------------------------
    // Створити
    // новий потік.
    //
    // УВАГА.
    //
    // Це ще НЕ запуск потоку.
    //
    // Ми лише створили
    // об'єкт.
    //--------------------------------------------------

    workerThread =
        new QThread(this);

    //--------------------------------------------------
    // Створити робітника.
    //
    // Саме він буде
    // виконувати processFolder().
    //--------------------------------------------------

    worker =
        new ProcessingWorker;

    //--------------------------------------------------
    // Передати шляхи.
    //
    // Після запуску потоку
    // робітник вже буде знати,
    // що потрібно обробляти.
    //--------------------------------------------------

    worker->setPaths(
        folderEdit->text().toStdString(),
        excelEdit->text().toStdString());

    //--------------------------------------------------
    // Найважливіший рядок.
    //
    // moveToThread()
    //
    // Переміщає ProcessingWorker
    // у другий потік.
    //
    // Саме завдяки цьому
    // GUI НЕ буде зависати.
    //--------------------------------------------------

    worker->moveToThread(workerThread);
        //--------------------------------------------------
    // Зв'язати сигнали та слоти.
    //
    // З цього моменту всі об'єкти
    // вже створені.
    //
    // Залишилось пояснити Qt,
    // хто кого буде викликати.
    //--------------------------------------------------

    //--------------------------------------------------
    // Коли QThread почне працювати,
    // Qt автоматично викличе:
    //
    // ProcessingWorker::process()
    //
    // Це набагато безпечніше,
    // ніж викликати process()
    // вручну.
    //--------------------------------------------------

    connect(workerThread,
            &QThread::started,
            worker,
            &ProcessingWorker::process);

    //--------------------------------------------------
    // ProcessingWorker повідомляє,
    // що потрібно додати рядок
    // до журналу.
    //
    // Наприклад:
    //
    // 1.pdf -> RENAMED
    //
    // або
    //
    // 7.pdf -> DUPLICATE
    //
    // Ми не звертаємось до журналу
    // напряму з другого потоку.
    //
    // Робітник лише надсилає сигнал.
    //
    // Qt сам безпечно передає його
    // у головний потік GUI.
    //--------------------------------------------------

    connect(worker,
            &ProcessingWorker::logMessage,
            logEdit,
            &QPlainTextEdit::appendPlainText);

    //--------------------------------------------------
    // Оновлення ProgressBar.
    //
    // Робітник надсилає число
    // від 0 до 100.
    //
    // Qt автоматично викликає:
    //
    // progressBar->setValue(...)
    //--------------------------------------------------

    connect(worker,
            &ProcessingWorker::progressChanged,
            progressBar,
            &QProgressBar::setValue);

    //--------------------------------------------------
    // Робота завершена.
    //
    // ProcessingWorker надсилає:
    //
    // finished()
    //
    // MainWindow змінить:
    //
    // • статус;
    // • кнопку;
    // • ProgressBar.
    //--------------------------------------------------

    connect(worker,
            &ProcessingWorker::finished,
            this,
            &MainWindow::processingFinished);

    //--------------------------------------------------
    // Після завершення роботи
    // потрібно завершити
    // QThread.
    //
    // finished()
    //
    //        ↓
    //
    // QThread::quit()
    //--------------------------------------------------

    connect(worker,
            &ProcessingWorker::finished,
            workerThread,
            &QThread::quit);

    //--------------------------------------------------
    // deleteLater()
    //
    // Чому НЕ delete?
    //
    // Тому що об'єкт може ще
    // обробляти події Qt.
    //
    // deleteLater()
    //
    // говорить Qt:
    //
    // "видали об'єкт,
    // коли це стане безпечно".
    //
    // Це стандартний спосіб
    // роботи у Qt.
    //--------------------------------------------------

    connect(workerThread,
            &QThread::finished,
            worker,
            &QObject::deleteLater);

    //--------------------------------------------------
    // Аналогічно
    // видаляємо сам потік.
    //--------------------------------------------------

    connect(workerThread,
            &QThread::finished,
            workerThread,
            &QObject::deleteLater);

    //--------------------------------------------------
    // Запуск потоку.
    //
    // Саме тут
    // починається виконання.
    //
    // Послідовність буде такою:
    //
    // start()
    //
    //      ↓
    //
    // started()
    //
    //      ↓
    //
    // process()
    //
    //      ↓
    //
    // finished()
    //
    //      ↓
    //
    // quit()
    //
    //      ↓
    //
    // finished()
    //
    //      ↓
    //
    // deleteLater()
    //--------------------------------------------------

    workerThread->start();
}
//======================================================
// processingFinished()
//
// Цей слот викликається автоматично,
// коли ProcessingWorker повідомляє,
// що вся робота завершена.
//
// Саме тут ми:
//
// • повертаємо GUI у початковий стан;
// • дозволяємо новий запуск;
// • очищаємо вказівники.
//
//======================================================

void MainWindow::processingFinished()
{
    //--------------------------------------------------
    // На випадок,
    // якщо ProgressBar
    // ще не дійшов до 100%.
    //--------------------------------------------------

    progressBar->setValue(100);

    //--------------------------------------------------
    // Змінити статус.
    //--------------------------------------------------

    statusLabel->setText("Готово");
        //--------------------------------------------------
    // Робота завершена.
    //
    // Наступне натискання
    // кнопки закриє програму.
    //--------------------------------------------------

    processingCompleted = true;

    //--------------------------------------------------
    // Знову дозволити
    // натискати кнопку.
    //--------------------------------------------------

    startButton->setEnabled(true);
        //--------------------------------------------------
    // Змінити напис кнопки.
    //--------------------------------------------------

    startButton->setText("Завершити");

    //--------------------------------------------------
    // ProcessingWorker
    // вже видалений через
    // QObject::deleteLater().
    //
    // Тому більше ним
    // користуватись не можна.
    //
    // nullptr захищає нас
    // від випадкового доступу
    // до вже видаленого об'єкта.
    //--------------------------------------------------

    worker = nullptr;

    //--------------------------------------------------
    // Аналогічно
    // очищаємо вказівник
    // на потік.
    //--------------------------------------------------

    workerThread = nullptr;
}