#ifndef UTILS_H
#define UTILS_H

#include <string>

//======================================================
// Utils
//
// Допоміжні функції, які використовуються
// різними модулями програми.
//======================================================

//------------------------------------------------------
// Перевіряє, чи існує файл.
//
// path - повний шлях до файлу.
//
// Повертає:
// true  - файл існує.
// false - файл відсутній.
//------------------------------------------------------
bool fileExists(const std::string& path);

//------------------------------------------------------
// Видаляє із назви символи,
// які заборонені у назвах файлів.
//
// Наприклад:
//
// / \ : * ? " < > |
//
// Повертає безпечну назву.
//------------------------------------------------------
std::string sanitizeFileName(const std::string& name);

//------------------------------------------------------
// Формує повний шлях нового PDF.
//
// folder - папка з PDF.
// personName - ПІБ.
//
// Повертає:
//
// folder/ПІБ.pdf
//------------------------------------------------------
std::string makePdfName(const std::string& folder,
                        const std::string& personName);

#endif